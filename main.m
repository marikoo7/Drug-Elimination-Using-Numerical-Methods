% main.m
clc; clear; close all;

% --------------------------
% القيم الابتدائية
% --------------------------
C0 = 100;        % التركيز الابتدائي
k = 0.2;         % معدل الإزالة
h = 1;           % خطوة الزمن
t_final = 24;    % الزمن النهائي
f = @(t, C) -k * C;   % المعادلة التفاضلية

% --------------------------
% حساب القيم بالطرق المختلفة (من الشخص الثاني)
% --------------------------
