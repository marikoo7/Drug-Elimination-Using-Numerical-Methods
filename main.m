[t2, y2] = Midpoint(f, 0, t_final, C0, h);
[t3, y3] = rung_cutto(f, 0, t_final, C0, h);

% --------------------------
% رسم النتائج (من الشخص الرابع)
% --------------------------
plot-connection(t1, y1, y2, y3);   % نمرر نفس t لو كلها متساوية

